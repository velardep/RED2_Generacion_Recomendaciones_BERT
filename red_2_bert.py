def build_X(items, E, w_area=W_AREA, w_dims=W_DIMS):
    X = []
    y = []
    for it, e in tqdm(list(zip(items, E)), total=len(items), desc="build_X"):
        A = build_area_vec(it["areas_top"]) * float(w_area)
        D = build_dims_vec(it["dims_probs"]) * float(w_dims)
        T = build_tipo_vec(it["tipo_fuente"])
        M = build_meta_vec(it["text"])

        x = np.concatenate([e, A, D, T, M], axis=0).astype(np.float32)
        X.append(x)
        y.append(label2id[it["label"]])

    return np.vstack(X), np.array(y, dtype=np.int64)


