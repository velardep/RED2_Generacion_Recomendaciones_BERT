def build_area_vec(areas_top):
    A = np.zeros(len(AREA_ORDER), dtype=np.float32)
    if isinstance(areas_top, list):
        for item in areas_top:
            area = item.get("area")
            score = item.get("score", 0.0)
            if area in area2idx:
                A[area2idx[area]] = float(score)
    return A

def build_dims_vec(dims_probs):
    # si no hay dims, ponemos uniforme 0.25 (neutral)
    D = np.array([0.25, 0.25, 0.25, 0.25], dtype=np.float32)
    if isinstance(dims_probs, dict):
        D = np.array([float(dims_probs.get(k, 0.25)) for k in DIM_ORDER], dtype=np.float32)
    return D

def build_tipo_vec(tipo_fuente):
    return np.array([1.0 if tipo_fuente == "mensaje" else 0.0], dtype=np.float32)

def build_meta_vec(text):
    # longitud suavizada (0..1 aprox)
    L = min(len(text) / 800.0, 1.0)
    return np.array([L], dtype=np.float32)

