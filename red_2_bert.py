# ---------- helpers ----------
def read_pdf_text(path: str, max_pages: int = None) -> str:
    reader = PdfReader(path)
    pages = reader.pages[:max_pages] if max_pages else reader.pages
    texts = []
    for p in pages:
        t = p.extract_text() or ""
        t = t.replace("\x00", " ").strip()
        if t:
            texts.append(t)
    return "\n\n".join(texts)

def chunk_text(text: str, target_chars: int = 1200, overlap: int = 200):
    text = (text or "").strip()
    if not text:
        return []
    chunks = []
    i = 0
    n = len(text)
    while i < n:
        j = min(i + target_chars, n)

        # intenta cortar en un separador "bonito"
        cut = j
        for sep in ["\n\n", "\n", ". "]:
            k = text.rfind(sep, i, j)
            if k != -1 and k > i + target_chars * 0.5:
                cut = k + len(sep)
                break

        chunk = text[i:cut].strip()
        if len(chunk) > 40:
            chunks.append(chunk)

        i = max(cut - overlap, cut)  # overlap real
        if i == cut:  # evita loop infinito
            i += 1
    return chunks

def softmax_np(x):
    x = np.array(x, dtype=np.float32)
    x = x - x.max()
    ex = np.exp(x)
    return ex / (ex.sum() + 1e-9)

def topk_labels(probs, k=5):
    idx = np.argsort(probs)[::-1][:k]
    return [(LABELS[i], float(probs[i])) for i in idx]

