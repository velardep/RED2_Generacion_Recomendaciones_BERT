def recommend_from_pdf(pdf_path: str, question: str = None, max_pages: int = None):
    full_text = read_pdf_text(pdf_path, max_pages=max_pages)
    chunks = chunk_text(full_text, target_chars=1200, overlap=200)

    if not chunks:
        raise ValueError("No pude extraer texto del PDF (puede ser escaneado).")

    # SBERT embeddings para similitud pregunta-chunk (si hay pregunta)
    if question and question.strip():
        q_emb = sbert.encode([question], convert_to_numpy=True, normalize_embeddings=True)[0]
        c_emb = sbert.encode(chunks, convert_to_numpy=True, normalize_embeddings=True)
        sims = (c_emb @ q_emb).astype(np.float32)  # cos sim por estar normalizados
        # pesos según similitud (softmax con temperatura)
        temp = 0.08  # más bajo = más "concentrado"
        weights = softmax_np(sims / temp)
        mode = "CON_PREGUNTA"
    else:
        sims = np.ones(len(chunks), dtype=np.float32)
        weights = np.ones(len(chunks), dtype=np.float32) / len(chunks)
        mode = "SIN_PREGUNTA_PROMEDIO"

    chunk_results = []
    probs_all = []

    for i, ch in enumerate(chunks):
        areas_top, dims_probs = red1_features_for_text(ch)
        probs, top = predict_proba(
            ch,
            areas_top=areas_top,
            dims_probs=dims_probs,
            tipo_fuente="archivo",   # porque viene de pdf
            top_k=5
        )
        probs_all.append(probs)
        chunk_results.append({
            "i": i,
            "w": float(weights[i]),
            "sim": float(sims[i]),
            "top": top,
            "preview": (ch[:240] + " ...") if len(ch) > 240 else ch
        })

    probs_all = np.vstack(probs_all)  # [n_chunks, n_labels]
    global_probs = (probs_all * weights.reshape(-1, 1)).sum(axis=0)
    global_probs = global_probs / (global_probs.sum() + 1e-9)

    # ordenar chunks por peso (si hay pregunta) o por prob de la etiqueta top global
    top_global = topk_labels(global_probs, k=5)
    best_label = top_global[0][0]
    best_label_idx = LABELS.index(best_label)

    # rank chunks por contribución a la etiqueta top
    contrib = probs_all[:, best_label_idx] * weights
    idx_rank = np.argsort(contrib)[::-1]

    print(f"\nMODE: {mode} | chunks: {len(chunks)}")
    if question and question.strip():
        print("PREGUNTA:", question)
    print("TOP global:", top_global)

    print("\n📌 Top chunks que soportan la recomendación principal:")
    for rank in range(min(5, len(chunks))):
        j = int(idx_rank[rank])
        print(f"\n  #{rank+1} chunk idx={j} weight={weights[j]:.3f} sim={sims[j]:.3f}")
        print("  top chunk:", chunk_results[j]["top"][:3])
        print("  preview:", chunk_results[j]["preview"])

    return global_probs, chunk_results

