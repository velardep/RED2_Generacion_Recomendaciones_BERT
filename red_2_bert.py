def embed_one(text: str):
    # usa tu SBERT ya cargado
    e = sbert.encode([text], convert_to_numpy=True, normalize_embeddings=True).astype(np.float32)[0]
    return e

def cosine(a, b):
    # a,b normalizados -> dot
    return float(np.dot(a, b))

def aggregate_for_pdf(chunks, question=None):
    # 1) probas por chunk (Red2)
    chunk_probs = []
    for ch in chunks:
        p, _ = predict_proba(ch, tipo_fuente="archivo", top_k=TOPK_LABELS_PRINT)
        chunk_probs.append(p)
    chunk_probs = np.vstack(chunk_probs)  # [N, C]

    # 2) sin pregunta => promedio simple
    if not question or not question.strip():
        w = np.ones(len(chunks), dtype=np.float32)
        w = w / (w.sum() + EPS)
        probs_global = (chunk_probs * w[:, None]).sum(axis=0)
        mode = "SIN_PREGUNTA_PROMEDIO"
        sims = np.ones(len(chunks), dtype=np.float32)
        idx_sorted = np.argsort(-w)
        return probs_global, w, sims, idx_sorted, mode

    # 3) con pregunta => pesos por similitud (query vs chunk)
    q = question.strip()
    qE = embed_one(q)
    cE = np.vstack([embed_one(ch) for ch in chunks])

    sims = np.array([cosine(qE, cE[i]) for i in range(len(chunks))], dtype=np.float32)
    idx = np.argsort(-sims)

    top1 = float(sims[idx[0]])
    top3 = float(sims[idx[min(2, len(idx)-1)]]) if len(idx) >= 3 else float(sims[idx[0]])
    gap = top1 - top3

    is_spec = (top1 >= SIM_TOP1_SPEC_TH) and (gap >= SIM_GAP_SPEC_TH)
    k = min(TOPK_SPEC if is_spec else TOPK_GEN, len(chunks))
    temp = TEMP_SPEC if is_spec else TEMP_GEN

    top_idx = idx[:k]
    top_sims = sims[top_idx]
    weights = _softmax(top_sims / max(temp, 1e-6)).astype(np.float32)

    probs_global = (chunk_probs[top_idx] * weights[:, None]).sum(axis=0)
    mode = "CON_PREGUNTA_ESPECIFICA" if is_spec else "CON_PREGUNTA_GENERAL"
    return probs_global, weights, top_sims, top_idx, mode

